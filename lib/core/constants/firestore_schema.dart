/// ══════════════════════════════════════════════════════════
/// COLLECTION 1: /users/{uid}
/// ══════════════════════════════════════════════════════════
/// PURPOSE: Stores all user profiles (admin + customer)
/// DOCUMENT ID: Firebase Auth UID
///
/// FULL DOCUMENT STRUCTURE:
/// {
///   // ── Identity ──────────────────────────────────────
///   "uid":           String,   // = document ID
///   "email":         String,   // lowercase, trimmed
///   "displayName":   String,   // min 2 chars
///   "photoUrl":      String?,  // Firebase Storage URL
///   "role":          String,   // ENUM: 'admin' | 'customer'
///   "emailVerified": bool,     // synced from Firebase Auth
///
///   // ── Session ───────────────────────────────────────
///   "fcmToken":      String?,  // updated on every login
///   "createdAt":     Timestamp,// FieldValue.serverTimestamp
///   "lastLoginAt":   Timestamp,// updated on every login
///
///   // ── Commerce ──────────────────────────────────────
///   "totalOrders":   int,      // incremented via transaction
///   "totalSpent":    double,   // incremented via transaction
///   "eliteStatus":   String,   // COMPUTED: see rules below
///                              // 'BRONZE'|'SILVER'|'GOLD'
///                              // |'PLATINUM'
///   "wishlistCount": int,      // incremented/decremented
///
///   // ── Addresses (embedded array, max 5) ─────────────
///   "addresses": [
///     {
///       "addressId":  String,  // UUID v4
///       "label":      String,  // 'Home'|'Office'|'Other'
///       "fullName":   String,  // recipient name
///       "phone":      String,  // with country code
///       "street":     String,
///       "city":       String,
///       "state":      String,
///       "zipCode":    String,
///       "country":    String,  // ISO 3166-1 alpha-2
///       "isDefault":  bool     // only 1 can be true
///     }
///   ],
///
///   // ── Notification Preferences ───────────────────────
///   "notificationPrefs": {
///     "orderUpdates":  bool,   // default: true
///     "promotions":    bool,   // default: true
///     "newArrivals":   bool    // default: false
///   }
/// }
///
/// ELITE STATUS RULES (computed on every order completion):
///   totalOrders 0-4   → 'BRONZE'
///   totalOrders 5-19  → 'SILVER'
///   totalOrders 20-49 → 'GOLD'
///   totalOrders 50+   → 'PLATINUM'
///
/// CONSTRAINTS:
///   - addresses array NEVER exceeds 5 items
///   - Only 1 address can have isDefault: true
///   - role NEVER changed by client — admin only via
///     Firebase Console or Cloud Function
///   - fcmToken is nullable (user may deny permissions)
///
/// ══════════════════════════════════════════════════════════
/// COLLECTION 2: /products/{productId}
/// ══════════════════════════════════════════════════════════
/// PURPOSE: All products, both active and archived
/// DOCUMENT ID: Auto-generated by Firestore
///
/// FULL DOCUMENT STRUCTURE:
/// {
///   // ── Identity ──────────────────────────────────────
///   "productId":     String,   // = document ID (set after create)
///   "name":          String,   // min 3 chars
///   "brand":         String,   // e.g. 'LUXR MAISON'
///   "description":   String,   // rich text (markdown allowed)
///   "category":      String,   // ENUM (see categories below)
///   "subcategory":   String?,  // optional
///   "tags":          String[], // for search: ['silk','dress',...]
///
///   // ── Search optimization ───────────────────────────
///   "searchIndex":   String[], // lowercase tokens for search
///                              // generated on write:
///                              // split name+brand+tags by space
///                              // store as lowercase array
///
///   // ── Pricing ───────────────────────────────────────
///   "price":         double,   // original price, always > 0
///   "discountPct":   int,      // 0-90 (percent off)
///   "finalPrice":    double,   // COMPUTED: price*(1-discount/100)
///                              // stored for query performance
///
///   // ── Media ─────────────────────────────────────────
///   "imageUrls":     String[], // min 1, Firebase Storage URLs
///   "thumbnailUrl":  String,   // first image = thumbnail
///
///   // ── Inventory (MAP — key = size, value = qty) ─────
///   "inventory": {
///     "XS": int,   // stock count per size
///     "S":  int,
///     "M":  int,
///     "L":  int,
///     "XL": int,
///     "XXL": int,
///     "ONE_SIZE": int  // for accessories/bags
///   },
///   "totalStock":    int,      // SUM of all inventory values
///                              // CRITICAL: updated atomically
///   "lowStockThreshold": int,  // default 5, admin can override
///
///   // ── Colors ────────────────────────────────────────
///   "colors": [
///     {
///       "name":      String,   // 'Obsidian Black'
///       "hexCode":   String,   // '#1A1A1A'
///     }
///   ],
///
///   // ── Status flags ──────────────────────────────────
///   "isActive":      bool,     // false = soft deleted/archived
///   "isFeatured":    bool,     // shows on home Featured section
///   "isNewArrival":  bool,     // shows on New Arrivals section
///   "isLimitedEdition": bool,
///
///   // ── Analytics ─────────────────────────────────────
///   "avgRating":     double,   // 0.0 - 5.0
///   "reviewCount":   int,      // total reviews
///   "soldCount":     int,      // total units sold (all time)
///   "viewCount":     int,      // total product page views
///
///   // ── Timestamps ────────────────────────────────────
///   "createdAt":     Timestamp,
///   "updatedAt":     Timestamp,
///   "createdBy":     String    // admin uid
/// }
///
/// VALID CATEGORIES:
///   'Apparel' | 'Footwear' | 'Accessories' | 'Bags' |
///   'Watches' | 'Jewelry' | 'Beauty' | 'Winter Wear'
///
/// INVENTORY RULES:
///   - totalStock = XS+S+M+L+XL+XXL+ONE_SIZE (always)
///   - When ANY size hits 0: do NOT remove key, keep at 0
///   - totalStock 0 = OUT OF STOCK
///   - totalStock 1-lowStockThreshold = LOW STOCK
///   - totalStock > lowStockThreshold = IN STOCK
///   - Stock updates MUST use Firestore transactions
///   - finalPrice MUST be recomputed on every price/discount update
///
/// ══════════════════════════════════════════════════════════
/// COLLECTION 3: /orders/{orderId}
/// ══════════════════════════════════════════════════════════
/// PURPOSE: All customer orders
/// DOCUMENT ID: Custom format "SC-" + timestamp + random4
/// Example: "SC-1698123456-A7K2"
///
/// FULL DOCUMENT STRUCTURE:
/// {
///   // ── Identity ──────────────────────────────────────
///   "orderId":       String,   // = document ID
///   "userId":        String,   // buyer's uid
///   "userEmail":     String,   // denormalized for admin queries
///   "userName":      String,   // denormalized
///
///   // ── Items (array of order items) ──────────────────
///   "items": [
///     {
///       "productId":   String,
///       "productName": String, // denormalized (snapshot)
///       "brand":       String, // denormalized
///       "imageUrl":    String, // first image
///       "size":        String,
///       "color":       String, // color name
///       "quantity":    int,    // >= 1
///       "unitPrice":   double, // price AT TIME OF ORDER
///       "discountPct": int,    // discount AT TIME OF ORDER
///       "finalPrice":  double, // unitPrice after discount
///       "lineTotal":   double  // finalPrice * quantity
///     }
///   ],
///
///   // ── Pricing Summary ───────────────────────────────
///   "subtotal":      double,   // sum of all lineTotals
///   "shippingCost":  double,   // 0 = free, 25 = express
///   "discountAmount":double,   // from promo codes (Phase 6)
///   "taxAmount":     double,   // 0 for MVP
///   "total":         double,   // subtotal+shipping-discount+tax
///
///   // ── Shipping ──────────────────────────────────────
///   "shippingMethod": String,  // 'standard' | 'express'
///   "shippingAddress": {       // SNAPSHOT of address at order time
///     "fullName":  String,
///     "phone":     String,
///     "street":    String,
///     "city":      String,
///     "state":     String,
///     "zipCode":   String,
///     "country":   String
///   },
///   "estimatedDelivery": {
///     "from":      Timestamp,
///     "to":        Timestamp
///   },
///
///   // ── Payment ───────────────────────────────────────
///   "paymentMethod": String,   // 'cod' | 'online'
///   "paymentStatus": String,   // 'pending'|'paid'|'refunded'
///   "transactionId": String?,  // from payment gateway
///
///   // ── Status & Timeline ─────────────────────────────
///   "status":        String,
///   // ENUM: 'pending'|'confirmed'|'processing'|
///   //       'packed'|'shipped'|'out_for_delivery'|
///   //       'delivered'|'cancelled'|'return_requested'|
///   //       'returned'
///
///   "statusHistory": [         // append-only log
///     {
///       "status":    String,
///       "timestamp": Timestamp,
///       "note":      String?,  // admin note
///       "updatedBy": String    // admin uid or 'system'
///     }
///   ],
///
///   // ── Courier (added when shipped) ──────────────────
///   "courier": {
///     "name":          String?,
///     "trackingNumber": String?,
///     "estimatedTime": String?  // "Today, 5:00 PM"
///   },
///
///   // ── Timestamps ────────────────────────────────────
///   "placedAt":      Timestamp, // FieldValue.serverTimestamp
///   "updatedAt":     Timestamp,
///   "deliveredAt":   Timestamp?
/// }
///
/// ORDER ID GENERATION:
///   String generateOrderId() {
///     final timestamp = DateTime.now()
///         .millisecondsSinceEpoch.toString()
///         .substring(7); // last 6 digits
///     final random = Random().nextInt(9999)
///         .toString().padLeft(4, '0');
///     return 'SC-$timestamp-$random'.toUpperCase();
///   }
///
/// CRITICAL RULES:
///   - Items array is a SNAPSHOT (never references products)
///   - Prices in items NEVER change after order placed
///   - statusHistory is APPEND-ONLY (never delete entries)
///   - Stock deduction happens in SAME transaction as
///     order document creation
///
/// ══════════════════════════════════════════════════════════
/// COLLECTION 4: /users/{uid}/cart/{cartItemId}
/// ══════════════════════════════════════════════════════════
/// PURPOSE: User's active cart (subcollection of user)
/// DOCUMENT ID: productId + "_" + size + "_" + colorName
/// Example: "abc123_M_ObsidianBlack"
/// (This prevents duplicate entries for same product+size+color)
///
/// FULL DOCUMENT STRUCTURE:
/// {
///   "cartItemId":    String,   // = document ID
///   "productId":     String,
///   "productName":   String,   // denormalized
///   "brand":         String,   // denormalized
///   "imageUrl":      String,   // thumbnail
///   "size":          String,
///   "color":         String,   // color name
///   "colorHex":      String,
///   "quantity":      int,      // >= 1
///   "unitPrice":     double,   // LIVE price (re-read on checkout)
///   "discountPct":   int,      // LIVE discount
///   "finalPrice":    double,   // COMPUTED
///   "addedAt":       Timestamp,
///   "updatedAt":     Timestamp
/// }
///
/// CART RULES:
///   - Max 20 items in cart
///   - If same productId+size+color added again:
///     INCREMENT quantity, don't add new doc
///   - Cart is NOT a reservation — stock is NOT held
///   - On checkout: re-validate prices from /products
///   - Cart survives logout (stored in Firestore, not memory)
///
