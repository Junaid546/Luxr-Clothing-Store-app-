///   2. isFeatured ASC + isActive ASC + createdAt DESC
///      (home featured section)
///   3. isNewArrival ASC + isActive ASC + createdAt DESC
///      (new arrivals section)
///   4. category ASC + finalPrice ASC + isActive ASC
///      (price sort ascending)
///   5. category ASC + finalPrice DESC + isActive ASC
///      (price sort descending)
///   6. category ASC + avgRating DESC + isActive ASC
///      (sort by rating)
///   7. isActive ASC + soldCount DESC
///      (best sellers)
///   8. tags ARRAY_CONTAINS + isActive ASC
///      (tag-based filtering)
///   9. searchIndex ARRAY_CONTAINS + isActive ASC
///      (search)
///   10. totalStock ASC + isActive ASC
///       (low stock admin view)
///
/// ORDERS collection:
///   1. userId ASC + placedAt DESC
///      (my orders screen)
///   2. status ASC + placedAt DESC
///      (admin filter by status)
///   3. userId ASC + status ASC + placedAt DESC
///      (customer filtered orders)
///   4. placedAt DESC
///      (admin all orders by date)
///
/// NOTIFICATIONS collection:
///   1. userId ASC + isRead ASC + createdAt DESC
///      (unread notifications)
///   2. userId ASC + createdAt DESC
///      (all notifications)
///
/// ANALYTICS collection:
///   1. date DESC
///      (most recent analytics first)

